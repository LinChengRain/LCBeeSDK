#import "AnimationTableViewController.h"
#import "DetailAnimationViewController.h"
@interface AnimationTableViewController ()
@property (nonatomic,strong) NSArray *dataArray;
@end

@implementation AnimationTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.dataArray = @[@"CAReplicatorLayer 复制层动画", @"CAEmitterLayer 粒子动画", @"CAGradientLayer 渐变动画"];
    self.tableView.tableFooterView = [[UIView alloc] init];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.dataArray.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *cellID = @"cellID";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellID];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:cellID];
    }
    cell.textLabel.text = self.dataArray[indexPath.row];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    DetailAnimationViewController *animationCtrl = [[DetailAnimationViewController alloc] init];
    animationCtrl.layerType = indexPath.row;
    animationCtrl.title = self.dataArray[indexPath.row];
    [self.navigationController pushViewController:animationCtrl animated:YES];
}



